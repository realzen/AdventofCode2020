# In the above example, these slopes would find 2, 7, 3, 4, and 2 tree(s) respectively; multiplied together, these produce the answer 336.

# What do you get if you multiply together the number of trees encountered on each of the listed slopes?
slope1_trees = 0
slope2_trees = 0
slope3_trees = 0
slope4_trees = 0
slope5_trees = 0

slope1 = 1
slope2 = 3
slope3 = 5
slope4 = 7
slope5 = 1
for i in list_lines:
    line = i
    size = len(i)-1
    index = list_lines.index(i) + 1
    if line[slope1] == '#':
        slope1_trees += 1
    slope1 += 1
    slope1 %= size

    if line[slope2] == '#':
        slope2_trees += 1
    slope2 += 3 
    slope2 %= size

    if line[slope3] == '#':
        slope3_trees += 1
    slope3 += 5
    slope3 %= size
    if line[slope4] == '#':
        slope4_trees += 1
    slope4 += 7
    slope4 %= size

    if index % 2 == 0:
        if line[slope5] == '#':
            slope5_trees += 1
        slope5 += 1
    slope5 %= size

print("Answer 2:",slope1_trees * slope2_trees * slope3_trees * slope4_trees * slope5_trees)